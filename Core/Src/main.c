static uint8_t control = 0;
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *handcan)
{
  uint8_t Data[8] = {0};
  HAL_StatusTypeDef HAL_RetVal;
  CAN_RxHeaderTypeDef RxMeg;
  if (handcan == &hcan)
  {
    HAL_RetVal = HAL_CAN_GetRxMessage(handcan, CAN_RX_FIFO0, &RxMeg, Data);
    if (HAL_OK == HAL_RetVal)
    {
      if (RxMeg.StdId == 0x2e7)
      {
        if (Data[2] == 3 && ((Data[3] & 0xFC) == 4))
        {
          control = 1;
        }
        else
        {
          control = 0;
        }
      }
    }
  }
}


void CAN_Config(void)
{
  CAN_FilterTypeDef hCAN1_Filter; // CAN1滤波器
	hCAN1_Filter.FilterIdHigh = 0x02E7 << 5;
	hCAN1_Filter.FilterIdLow = 0;
	hCAN1_Filter.FilterMaskIdHigh = 0x02E8 << 5;
	hCAN1_Filter.FilterMaskIdLow = 0;
	hCAN1_Filter.FilterFIFOAssignment = CAN_FILTER_FIFO0;
	hCAN1_Filter.FilterBank = 1;
	hCAN1_Filter.FilterMode = CAN_FILTERMODE_IDLIST;
	hCAN1_Filter.FilterScale = CAN_FILTERSCALE_32BIT;
	hCAN1_Filter.FilterActivation = ENABLE;
  hCAN1_Filter.SlaveStartFilterBank = 14;
	HAL_CAN_ConfigFilter(&hcan, &hCAN1_Filter);

  HAL_CAN_ActivateNotification(&hcan, CAN_IT_RX_FIFO0_MSG_PENDING | CAN_IT_RX_FIFO0_FULL | CAN_IT_RX_FIFO0_OVERRUN);

  HAL_CAN_Start(&hcan);
}
